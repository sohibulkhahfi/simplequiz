// Fungsi untuk mengelompokkan data menjadi beberapa grup dengan ukuran tertentu
function groupDataIntoLevels(data, groupSize) {
  const groupedData = {};
  let currentGroup = 1;

  for (let i = 0; i < data.length; i++) {
    if (!groupedData[`level${currentGroup}`]) {
      groupedData[`level${currentGroup}`] = [];
    }

    groupedData[`level${currentGroup}`].push(data[i]);

    // Pindah ke grup berikutnya setelah mencapai ukuran grup yang diinginkan
    if (groupedData[`level${currentGroup}`].length === groupSize) {
      currentGroup++;
    }
  }

  return groupedData;
}

